# ── Core: Warp Ad into Bounding Box ─────────────────────────────────────────
def warp_ad_into_bbox(
    frame: np.ndarray,
    ad_img: np.ndarray,
    x1: int, y1: int, x2: int, y2: int,
    blend_alpha: float = 0.88,
    edge_fade: bool = True,
) -> np.ndarray:
    """
    Warp `ad_img` into the axis-aligned bounding box [x1,y1,x2,y2] on `frame`.

    Parameters
    ----------
    blend_alpha : 0.0–1.0  — 1.0 = fully replace, lower = ghosted blend
    edge_fade   : feather edges for a more natural look
    """
    frame = frame.copy()
    bw, bh = x2 - x1, y2 - y1
    if bw < 10 or bh < 10:
        return frame

    # ── Four corners of the destination bounding box ──────────────────────
    dst_pts = np.array([
        [x1, y1],
        [x2, y1],
        [x2, y2],
        [x1, y2],
    ], dtype=np.float32)

    # ── Four corners of the source ad image ───────────────────────────────
    ah, aw = ad_img.shape[:2]
    src_pts = np.array([
        [0,  0 ],
        [aw, 0 ],
        [aw, ah],
        [0,  ah],
    ], dtype=np.float32)

    # ── Perspective transform ─────────────────────────────────────────────
    H, _ = cv2.findHomography(src_pts, dst_pts)
    warped = cv2.warpPerspective(ad_img, H, (frame.shape[1], frame.shape[0]))

    # ── Build mask for the bounding-box region ────────────────────────────
    mask = np.zeros(frame.shape[:2], dtype=np.uint8)
    cv2.fillConvexPoly(mask, dst_pts.astype(np.int32), 255)

    if edge_fade:
        # Slight gaussian blur on mask edges for soft compositing
        mask = cv2.GaussianBlur(mask, (9, 9), 0)

    # ── Alpha composite ───────────────────────────────────────────────────
    mask_f   = mask.astype(np.float32) / 255.0
    mask_3   = mask_f[:, :, np.newaxis]
    frame_f  = frame.astype(np.float32)
    warped_f = warped.astype(np.float32)

    composited = (
        warped_f  * mask_3 * blend_alpha +
        frame_f   * mask_3 * (1.0 - blend_alpha) +
        frame_f   * (1.0 - mask_3)
    )
    return np.clip(composited, 0, 255).astype(np.uint8)


# ── Draw Detection Info ───────────────────────────────────────────────────────
def draw_info_overlay(
    frame: np.ndarray,
    label: str,
    conf: float,
    x1: int, y1: int, x2: int, y2: int,
) -> np.ndarray:
    """Draw a small label badge above the bounding box."""
    tag   = f"{label} {conf:.2f}"
    font  = cv2.FONT_HERSHEY_SIMPLEX
    scale = 0.55
    thick = 1
    (tw, th), baseline = cv2.getTextSize(tag, font, scale, thick)

    # Badge background
    pad = 4
    by1 = max(0, y1 - th - 2 * pad)
    by2 = y1
    bx2 = min(frame.shape[1], x1 + tw + 2 * pad)
    cv2.rectangle(frame, (x1, by1), (bx2, by2), (0, 180, 80), -1)
    cv2.putText(frame, tag, (x1 + pad, y1 - pad), font, scale, (255, 255, 255), thick, cv2.LINE_AA)

    # Thin box outline around replaced region
    cv2.rectangle(frame, (x1, y1), (x2, y2), (0, 200, 90), 2)
    return frame


# ── Process Single Image ──────────────────────────────────────────────────────
def process_image(
    image_path: str | Path,
    model,
    ad_img: np.ndarray,
    blend_alpha: float = 0.88,
    save_dir: Path | None = None,
    show: bool = False,
) -> np.ndarray:
    image_path = Path(image_path)
    frame = cv2.imread(str(image_path))
    if frame is None:
        print(f"  ⚠️  Cannot read {image_path}")
        return None

    results = model(frame, verbose=False)[0]
    n_dets  = len(results.boxes)
    print(f"  📸 {image_path.name}: {n_dets} detection(s)")

    for box in results.boxes:
        x1, y1, x2, y2 = map(int, box.xyxy[0].tolist())
        conf  = float(box.conf[0])
        cls   = int(box.cls[0])
        label = model.names[cls]

        frame = warp_ad_into_bbox(frame, ad_img, x1, y1, x2, y2, blend_alpha)
        frame = draw_info_overlay(frame, label, conf, x1, y1, x2, y2)

    if save_dir:
        out_path = save_dir / f"ad_{image_path.stem}.jpg"
        cv2.imwrite(str(out_path), frame)
        print(f"    💾 Saved → {out_path}")

    if show:
        try:
            from google.colab.patches import cv2_imshow
            cv2_imshow(frame)
        except ImportError:
            cv2.imshow("Ad Replaced", frame)
            cv2.waitKey(0)

    return frame


