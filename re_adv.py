# ── Process Video ─────────────────────────────────────────────────────────────
def process_video(
    video_path: str | Path,
    model,
    ad_img: np.ndarray,
    blend_alpha: float = 0.88,
    output_path: str | Path | None = None,
    max_frames: int | None = None,
    show_progress: bool = True,
):
    video_path  = Path(video_path)
    output_path = Path(output_path) if output_path else AD_OUTPUT_DIR / f"ad_{video_path.name}"
    output_path.parent.mkdir(parents=True, exist_ok=True)

    cap = cv2.VideoCapture(str(video_path))
    if not cap.isOpened():
        raise IOError(f"Cannot open video: {video_path}")

    fps    = cap.get(cv2.CAP_PROP_FPS) or 25.0
    width  = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total  = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

    fourcc = cv2.VideoWriter_fourcc(*"mp4v")
    writer = cv2.VideoWriter(str(output_path), fourcc, fps, (width, height))

    print(f"\n🎬 Processing video: {video_path.name}  ({width}×{height} @ {fps:.1f} fps)")
    print(f"   Total frames : {total}" + (f"  (capped at {max_frames})" if max_frames else ""))
    print(f"   Output       : {output_path}")

    frame_idx = 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        if max_frames and frame_idx >= max_frames:
            break

        results = model(frame, verbose=False)[0]
        for box in results.boxes:
            x1, y1, x2, y2 = map(int, box.xyxy[0].tolist())
            conf  = float(box.conf[0])
            cls   = int(box.cls[0])
            label = model.names[cls]
            frame = warp_ad_into_bbox(frame, ad_img, x1, y1, x2, y2, blend_alpha)
            frame = draw_info_overlay(frame, label, conf, x1, y1, x2, y2)

        writer.write(frame)
        frame_idx += 1

        if show_progress and frame_idx % 30 == 0:
            pct = (frame_idx / total * 100) if total > 0 else 0
            print(f"   Frame {frame_idx}/{total}  ({pct:.1f}%)")

    cap.release()
    writer.release()
    print(f"\n✅ Video saved → {output_path}")
    return output_path


