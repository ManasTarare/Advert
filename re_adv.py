# 2. QUAD DETECTION — find 4 actual corners inside a bounding box
# ─────────────────────────────────────────────────────────────────────────────
def order_corners(pts: np.ndarray) -> np.ndarray:
    """
    Order 4 points as: top-left, top-right, bottom-right, bottom-left.
    Works for any quadrilateral (tilted, perspective-distorted, etc.)
    """
    pts  = pts.reshape(4, 2).astype(np.float32)
    rect = np.zeros((4, 2), dtype=np.float32)
    s    = pts.sum(axis=1)
    diff = np.diff(pts, axis=1).ravel()
    rect[0] = pts[np.argmin(s)]      # top-left     (min x+y)
    rect[2] = pts[np.argmax(s)]      # bottom-right (max x+y)
    rect[1] = pts[np.argmin(diff)]   # top-right    (min x-y)
    rect[3] = pts[np.argmax(diff)]   # bottom-left  (max x-y)
    return rect


def detect_quad_in_roi(
    image: np.ndarray,
    x1: int, y1: int, x2: int, y2: int,
    debug: bool = False,
) -> np.ndarray:
    """
    Try to find a 4-corner quadrilateral inside the bounding box [x1,y1,x2,y2].

    Strategy:
      1. Crop the ROI with a small margin
      2. Blur → Canny edges
      3. Find external contours → pick largest area quadrilateral
      4. If no good quad, fall back to the bbox corners themselves

    Returns ordered (4,2) float32 array in FULL IMAGE coordinates.
    """
    pad   = 6
    rx1   = max(0, x1 - pad)
    ry1   = max(0, y1 - pad)
    rx2   = min(image.shape[1], x2 + pad)
    ry2   = min(image.shape[0], y2 + pad)
    roi   = image[ry1:ry2, rx1:rx2]

    if roi.size == 0:
        return _bbox_corners(x1, y1, x2, y2)

    gray  = cv2.cvtColor(roi, cv2.COLOR_BGR2GRAY)
    blur  = cv2.GaussianBlur(gray, (5, 5), 0)

    # ── Adaptive Canny thresholds ─────────────────────────────────────────────
    med         = float(np.median(blur))
    low, high   = max(0, 0.5 * med), min(255, 1.5 * med)
    edges       = cv2.Canny(blur, low, high)
    edges       = cv2.dilate(edges, np.ones((3, 3), np.uint8), iterations=1)

    contours, _ = cv2.findContours(edges, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not contours:
        return _bbox_corners(x1, y1, x2, y2)

    roi_area    = (rx2 - rx1) * (ry2 - ry1)
    best_quad   = None
    best_area   = 0

    for cnt in contours:
        area = cv2.contourArea(cnt)
        if area < roi_area * 0.10:          # skip tiny noise contours
            continue
        peri  = cv2.arcLength(cnt, True)
        approx = cv2.approxPolyDP(cnt, 0.04 * peri, True)
        if len(approx) == 4 and area > best_area:
            best_area = area
            best_quad = approx

    if best_quad is None:
        # No 4-sided contour — try largest contour's convex hull → bbox
        largest = max(contours, key=cv2.contourArea)
        hull    = cv2.convexHull(largest)
        peri    = cv2.arcLength(hull, True)
        approx  = cv2.approxPolyDP(hull, 0.04 * peri, True)
        if len(approx) == 4:
            best_quad = approx
        else:
            return _bbox_corners(x1, y1, x2, y2)

    # ── Convert ROI-local coords → full image coords ──────────────────────────
    pts_full = best_quad.reshape(4, 2).astype(np.float32)
    pts_full[:, 0] += rx1
    pts_full[:, 1] += ry1

    # ── Sanity check: quad must be mostly inside the bbox ─────────────────────
    cx = pts_full[:, 0].mean()
    cy = pts_full[:, 1].mean()
    if not (x1 <= cx <= x2 and y1 <= cy <= y2):
        return _bbox_corners(x1, y1, x2, y2)

    return order_corners(pts_full)


def _bbox_corners(x1, y1, x2, y2) -> np.ndarray:
    """Fallback: axis-aligned bounding box corners (same as Part 4)."""
    return np.array([
        [x1, y1], [x2, y1],
        [x2, y2], [x1, y2],
    ], dtype=np.float32)


# ─────────────────────────────────────────────────────────────────────────────
# 3. PERSPECTIVE WARP + COMPOSITE
# ─────────────────────────────────────────────────────────────────────────────
def warp_ad_perspective(
    frame: np.ndarray,
    ad_img: np.ndarray,
    dst_corners: np.ndarray,       # ordered (4,2) in image coords
    blend_alpha: float = 0.92,
    edge_fade: bool = True,
) -> np.ndarray:
    """
    Warp ad_img so its 4 corners map to dst_corners on frame.
    Uses full perspective homography — handles tilt, rotation, foreshortening.
    """
    frame   = frame.copy()
    ah, aw  = ad_img.shape[:2]

    src_pts = np.array([
        [0,  0 ], [aw, 0 ],
        [aw, ah], [0,  ah],
    ], dtype=np.float32)

    H, _    = cv2.findHomography(src_pts, dst_corners)
    if H is None:
        return frame

    warped  = cv2.warpPerspective(ad_img, H, (frame.shape[1], frame.shape[0]))

    # ── Mask for the destination quadrilateral ────────────────────────────────
    mask    = np.zeros(frame.shape[:2], dtype=np.uint8)
    cv2.fillConvexPoly(mask, dst_corners.astype(np.int32), 255)

    if edge_fade:
        mask = cv2.GaussianBlur(mask, (11, 11), 0)

    mask_f   = mask.astype(np.float32) / 255.0
    mask_3   = mask_f[:, :, np.newaxis]
    result   = (
        warped.astype(np.float32)  * mask_3 * blend_alpha +
        frame.astype(np.float32)   * mask_3 * (1.0 - blend_alpha) +
        frame.astype(np.float32)   * (1.0 - mask_3)
    )
    return np.clip(result, 0, 255).astype(np.uint8)


def draw_quad_overlay(
    frame: np.ndarray,
    corners: np.ndarray,
    label: str,
    conf: float,
    used_quad: bool,
) -> np.ndarray:
    """Draw the detected quad outline + label badge."""
    pts   = corners.astype(np.int32).reshape((-1, 1, 2))
    color = (0, 220, 100) if used_quad else (0, 160, 255)   # green=quad, orange=bbox fallback
    cv2.polylines(frame, [pts], isClosed=True, color=color, thickness=2)

    tag   = f"{label} {conf:.2f} {'[QUAD]' if used_quad else '[BBOX]'}"
    tl    = tuple(corners[0].astype(int))
    cv2.putText(frame, tag, (tl[0], max(0, tl[1] - 8)),
                cv2.FONT_HERSHEY_SIMPLEX, 0.55, color, 1, cv2.LINE_AA)
    return frame


# ─────────────────────────────────────────────────────────────────────────────
# 4. AUTO-TUNE (billboard-only, same logic as Part 4 cell)
# ─────────────────────────────────────────────────────────────────────────────
def auto_tune(image, model):
    model.conf = 0.05
    results    = model(image, verbose=False)[0]
    scores     = [float(b.conf[0]) for b in results.boxes
                  if model.names[int(b.cls[0])] == "billboard"]

    if not scores:
        conf_thr = 0.10
    elif len(scores) == 1:
        conf_thr = max(0.10, scores[0] - 0.05)
    else:
        scores.sort(reverse=True)
        conf_thr = max(0.10, scores[len(scores)//2] - 0.08)

    final = []
    for b in results.boxes:
        lbl  = model.names[int(b.cls[0])]
        conf = float(b.conf[0])
        if lbl == "billboard" and conf >= conf_thr:
            x1, y1, x2, y2 = map(int, b.xyxy[0].tolist())
            final.append((x1, y1, x2, y2, conf, lbl))

    gray  = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    alpha = float(np.clip(0.80 + (np.std(gray) / 128) * 0.16, 0.80, 0.96))
    return round(conf_thr, 2), round(alpha, 2), final


# ─────────────────────────────────────────────────────────────────────────────
# 5. FULL PIPELINE — process one image
# ─────────────────────────────────────────────────────────────────────────────
def process_image_p5(image, ad_img, model, debug=False):
    """
    Returns (replaced_frame, report_lines, n_quad, n_bbox_fallback)
    """
    conf_thr, alpha, boxes = auto_tune(image, model)
    report = [f"Auto-settings → conf ≥ {conf_thr}  |  blend α = {alpha:.2f}  |  {len(boxes)} billboard(s)"]

    replaced      = image.copy()
    n_quad        = 0
    n_bbox_fb     = 0

    for (x1, y1, x2, y2, conf_val, label) in boxes:
        # ── Try quad detection ────────────────────────────────────────────────
        corners   = detect_quad_in_roi(image, x1, y1, x2, y2, debug)
        bbox_corn = _bbox_corners(x1, y1, x2, y2)

        # Determine if we got a real quad or fell back
        used_quad = not np.allclose(corners, bbox_corn, atol=2.0)
        if used_quad:
            n_quad   += 1
            mode_str  = "QUAD (perspective)"
        else:
            n_bbox_fb += 1
            mode_str  = "BBOX fallback"

        area = (x2 - x1) * (y2 - y1)
        report.append(f"  • {label} ({conf_val:.2f})  {mode_str}  area={area}px²")
        report.append(f"    corners: {corners.astype(int).tolist()}")

        replaced = warp_ad_perspective(replaced, ad_img, corners, alpha)
        replaced = draw_quad_overlay(replaced, corners, label, conf_val, used_quad)

    return replaced, report, n_quad, n_bbox_fb


