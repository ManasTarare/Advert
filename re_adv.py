# ─── Auto-tune: pick conf & blend per image ───────────────────────────────────
def auto_tune(image: np.ndarray, model):
    """
    Run inference at low conf, then pick:
      • conf_threshold  — just below the highest-confidence billboard score
                          so we keep real billboards but drop noise
      • blend_alpha     — brighter/higher-contrast scenes get higher alpha
    Returns (conf_used, alpha_used, filtered_boxes)
    where filtered_boxes = list of (x1,y1,x2,y2, conf_val, label) for 'billboard' only
    """
    # ── 1. Run at very low conf to see all possible detections ────────────────
    model.conf = 0.05
    results = model(image, verbose=False)[0]

    billboard_scores = []
    for box in results.boxes:
        cls   = int(box.cls[0])
        label = model.names[cls]
        if label == "billboard":
            billboard_scores.append(float(box.conf[0]))

    # ── 2. Decide confidence threshold ────────────────────────────────────────
    if not billboard_scores:
        # No billboards found even at 0.05 — use a loose threshold anyway
        conf_threshold = 0.10
    elif len(billboard_scores) == 1:
        # Only one billboard: accept it if score > 0.20, else lower to capture it
        conf_threshold = max(0.10, billboard_scores[0] - 0.05)
    else:
        # Multiple billboards: set threshold just below the median score
        # so we keep genuine ones and skip borderline noise
        billboard_scores.sort(reverse=True)
        median_score   = billboard_scores[len(billboard_scores) // 2]
        conf_threshold = max(0.10, median_score - 0.08)

    # ── 3. Collect final billboard boxes above threshold ──────────────────────
    final_boxes = []
    for box in results.boxes:
        cls      = int(box.cls[0])
        label    = model.names[cls]
        conf_val = float(box.conf[0])
        if label == "billboard" and conf_val >= conf_threshold:
            x1, y1, x2, y2 = map(int, box.xyxy[0].tolist())
            final_boxes.append((x1, y1, x2, y2, conf_val, label))

    # ── 4. Auto blend alpha: based on image brightness variance ──────────────
    # High-contrast outdoor scenes → higher alpha (ad stands out)
    # Low-light / indoor → slightly lower alpha (blend in)
    gray      = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    std_dev   = float(np.std(gray))          # 0–128 typical range
    alpha     = np.clip(0.78 + (std_dev / 128) * 0.18, 0.78, 0.96)

    return round(conf_threshold, 2), round(alpha, 2), final_boxes


