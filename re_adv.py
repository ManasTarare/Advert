def order_corners(pts):
    pts  = pts.reshape(4, 2).astype(np.float32)
    rect = np.zeros((4, 2), dtype=np.float32)
    s    = pts.sum(axis=1)
    d    = np.diff(pts, axis=1).flatten()
    rect[0] = pts[np.argmin(s)]   # TL
    rect[2] = pts[np.argmax(s)]   # BR
    rect[1] = pts[np.argmin(d)]   # TR
    rect[3] = pts[np.argmax(d)]   # BL
    return rect

def get_billboard_corners(img_path, model, conf=0.25,
                          canny_lo=30, canny_hi=100, poly_eps=0.03):
    img = cv2.imread(img_path)
    if img is None:
        return [], None
    H, W   = img.shape[:2]
    results = model(img_path, conf=conf, verbose=False)[0]
    detections = []

    for box, conf_score in zip(
        results.boxes.xyxy.cpu().numpy(),
        results.boxes.conf.cpu().numpy()
    ):
        x1, y1, x2, y2 = box.astype(int)
        x1, y1 = max(0, x1), max(0, y1)
        x2, y2 = min(W, x2), min(H, y2)
        roi = img[y1:y2, x1:x2]
        if roi.size == 0:
            continue

        gray   = cv2.cvtColor(roi, cv2.COLOR_BGR2GRAY)
        blur   = cv2.GaussianBlur(gray, (5, 5), 0)
        edges  = cv2.Canny(blur, canny_lo, canny_hi)
        kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (3, 3))
        edges  = cv2.dilate(edges, kernel, iterations=1)

        contours, _ = cv2.findContours(edges, cv2.RETR_EXTERNAL,
                                       cv2.CHAIN_APPROX_SIMPLE)
        corners, method = None, 'bbox_fallback'

        if contours:
            contours = sorted(contours, key=cv2.contourArea, reverse=True)
            for cnt in contours[:3]:
                peri   = cv2.arcLength(cnt, True)
                approx = cv2.approxPolyDP(cnt, poly_eps * peri, True)
                if len(approx) == 4:
                    roi_h, roi_w = roi.shape[:2]
                    if cv2.contourArea(approx) / (roi_w * roi_h + 1e-6) > 0.1:
                        pts     = approx.reshape(4, 2) + np.array([x1, y1])
                        corners = order_corners(pts)
                        method  = 'contour'
                        break

        if corners is None:
            corners = order_corners(np.array(
                [[x1,y1],[x2,y1],[x2,y2],[x1,y2]], dtype=np.float32))

        detections.append({
            'bbox'   : (x1, y1, x2, y2),
            'corners': corners,
            'method' : method,
            'conf'   : float(conf_score),
        })

    return detections, img

def visualize_corners(img_path, model, conf=0.25):
    detections, img = get_billboard_corners(img_path, model, conf=conf)
    if img is None:
        print(f"❌ Cannot read {img_path}")
        return []

    vis = img.copy()
    corner_colors = [(0,255,0),(255,0,0),(0,0,255),(255,165,0)]
    labels        = ['TL','TR','BR','BL']

    for det in detections:
        x1, y1, x2, y2 = det['bbox']
        corners = det['corners'].astype(int)
        color   = (0,200,255) if det['method'] == 'contour' else (100,100,255)
        cv2.rectangle(vis, (x1,y1), (x2,y2), color, 2)
        for i in range(4):
            cv2.line(vis, tuple(corners[i]), tuple(corners[(i+1)%4]), (0,255,0), 2)
        for pt, col, lbl in zip(corners, corner_colors, labels):
            cv2.circle(vis, tuple(pt), 8, col, -1)
            cv2.putText(vis, lbl, (pt[0]+5, pt[1]-5),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.6, col, 2)
        cv2.putText(vis, f"conf:{det['conf']:.2f} [{det['method']}]",
                    (x1, y1-10), cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255,255,0), 1)

    plt.figure(figsize=(12, 8))
    plt.imshow(cv2.cvtColor(vis, cv2.COLOR_BGR2RGB))
    plt.axis('off')
    plt.title(f"Corners — {os.path.basename(img_path)}")
    plt.tight_layout()
    plt.show()

    print(f"Found {len(detections)} billboard(s):")
    for i, det in enumerate(detections):
        print(f"  [{i}] conf={det['conf']:.3f}  method={det['method']}")
        for lbl, pt in zip(labels, det['corners']):
            print(f"       {lbl}: ({pt[0]:.0f}, {pt[1]:.0f})")

    return detections

